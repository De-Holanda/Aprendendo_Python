# While = Enquanto
# Sinal de igualdade (=) em progrmação é uma atribuição, ou seja, tudo após tal sinal significa que está dando valor para o que está a esquerda 
# += está incrementando ou adicionando na variável    
    
contador = 0

while contador < 5:
    print(f"Contador esta em {contador}")
    contador += 1